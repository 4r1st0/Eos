    private void SaveOutputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveOutputActionPerformed
        //============START: Read data from GUI=================================
        header = textFieldHeader.getText();
        try {
            numberOfSpheres = Integer.parseInt(textFieldNumberOfSpheres.getText());
            numberOfEnergyBins = Integer.parseInt(textFieldEnergyBins.getText());
            dataCorrectionFactor = Double.parseDouble(textFieldDataCorrectionFactor.getText());
        } catch (NumberFormatException e) {
        // Invalid double String.
        }     
        
        for (String line : textAreaDetectorName.getText().split("\\n")) {
            if(line.length()>8){
                line = line.replaceAll("\\s+","");                              // remove space and hidden blank characters
                sphereNames.add(line.substring(0,8));                           // Format restriction: DetectorName is only valid with max. 8 characters
            } else {
                line = line.replaceAll("\\s+","");                              // remove space and hidden blank characters
                sphereNames.add(line);
            }
        }
        for (String line : textAreaDiameter.getText().split("\\n")) {
        sphereDiameter.add(Double.parseDouble(line));
        }
        for (String line : textAreaDetectorReading.getText().split("\\n")) {
            sphereCounts.add(Double.parseDouble(line));
        }
        for (String line : textAreaAbsUncertainty.getText().split("\\n")) {
            sphereAbsUncertainty.add(Double.parseDouble(line));
        }
        for (String line : textAreaPerUncertainty.getText().split("\\n")) {
            spherePerUncertainty.add(Double.parseDouble(line));
        }
        for (String line : textAreaResponseUncertainty.getText().split("\\n")) {
            responsePerUncertainty.add(Double.parseDouble(line));
        }
        //============END: Read data from GUI===================================
        
        // CHECK: UMG Output requested?
        if(checkboxUMGOut.getState()) {     
            //============START: Write data to file=============================            
            try{
                WriteInputFile writeUMGfile = new WriteInputFile();
                writeUMGfile.writeUMG(textFieldOutputName,
                                header, numberOfSpheres, dataCorrectionFactor,
                                sphereNames, sphereDiameter, sphereCounts,
                                sphereAbsUncertainty, spherePerUncertainty,
                                responsePerUncertainty);
            } catch (Exception e) {
                System.out.println("problem with path : "+ textFieldOutputName.getText());
                System.out.println(e.getMessage());
            }
            //============END: Write data to file===============================
        }  
        
        // CHECK: WinBUGS Output requested?
        if(checkboxWinBUGSOut.getState()) {
            //============START: Write data to file=============================
            try{
                WriteInputFile writeWINfile = new WriteInputFile();
                writeWINfile.writeWinBUGS(textFieldOutputName,
                                    header, numberOfEnergyBins, numberOfSpheres, 
                                    sphereCounts, spherePerUncertainty);
            } catch (Exception e) {
                System.out.println("problem with path : "+ textFieldOutputName.getText());
                System.out.println(e.getMessage());
            }
            //============END: Write data to file===============================
        }
        
        //=============START: Information Messages==============================
        if(checkboxWinBUGSOut.getState() == false && checkboxUMGOut.getState() == false) {
            JOptionPane.showMessageDialog(null, "No output format selected!", "Information", JOptionPane.INFORMATION_MESSAGE);         
        }
        if(numberOfSpheres == 0) {
            JOptionPane.showMessageDialog(null, "Number of Spheres equals zero or empty!\n" + "No data written to file.", "Information", JOptionPane.INFORMATION_MESSAGE);         
        }
        if(header.length() > 80) {
            JOptionPane.showMessageDialog(null, "Header length greater 80 characters.\n" + "Header shortened to max. 80 characters.", "Information", JOptionPane.INFORMATION_MESSAGE);         
        }
        //=============END: Information Messages================================
        
        //============START: clear ArrayLists===================================
        sphereNames = new ArrayList<String>();
        sphereDiameter = new ArrayList<Double>();
        sphereCounts = new ArrayList<Double>();
        sphereAbsUncertainty = new ArrayList<Double>();
        spherePerUncertainty = new ArrayList<Double>();
        responsePerUncertainty = new ArrayList<Double>();
        //============END: clear ArrayLists=====================================
    }//GEN-LAST:event_SaveOutputActionPerformed

